using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace scheduler.Utilities
{
    public static class Utilities
    {

        /// <summary>
        /// Function to trim trailing numbers from a string
        /// </summary>
        /// <param name="input">input string</param>
        /// <returns>input string with trailing numbers removed</returns>
        public static string TrimTrailingNumbers(string input)
        {
            // array of characters to remove
            char[] charsToTrim = { '1', '2', '3', '4', '5', '6', '7', '8', '9', '0' };

            return input.TrimEnd(charsToTrim);
        }
    }
}
